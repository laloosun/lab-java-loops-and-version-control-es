public class Main { // We start by defining a Java class called Main
    public static void main(String[] args) { // In the main method, we initialize two integer variables x and y with values 3 and 5 respectively.
        int x = 3; // Assigning a value of 3 to variable x
        int y = 5; // Assigning a value of 5 to variable y

        int result = calculateExpression(x, y); // We then call the calculateExpression method passing in x and y.
        System.out.println("Result: " + result); // Printing the result
    }

    public static int calculateExpression(int x, int y) {  // In the calculateExpression method:
        int firstPart = x * x; // Calculating x^2, calculate the square of x and store it in firstPart.
        int secondPart = ((4 * y) / 5) - x; // Calculating (((4*y)/5)-x), store it in secondPart
        int result = firstPart + (secondPart * secondPart); // add the squares of both parts to get the final result

        return result; // The final result is returned and printed in the main method.
    }
}

